#include <stdio.h>
int main()
{
    double I, M = 0;
    printf("请输入利润:");
    scanf("%d", &I);
    if (I > 1000000)
    {
        M = (I - 1000000) * 0.01;
        I = 1000000;
    }
    if (I > 600000)
    {
        M += (I - 600000) * 0.015;
        I = 600000;
    }
    if (I > 400000)
    {
        M += (I - 400000) * 0.03;
        I = 400000;
    }
    if (I > 200000)
    {
        M += (I - 200000) * 0.05;
        I = 200000;
    }
    if (I > 100000)
    {
        M += (I - 100000) * 0.075;
        I = 100000;
    }
    if (I > 0)
    {
        M += I * 0.075;
        I = 100000;
    }
    printf("应发奖金数为:%f", M);
    return 0;
}